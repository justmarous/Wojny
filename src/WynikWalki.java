public class WynikWalki {
    private int hpDamage;
    private int initCost;

    public WynikWalki(int hpDamage, int initCost) {
        this.hpDamage = hpDamage;
        this.initCost = initCost;
    }

    public int getHpDamage() {
        return hpDamage;
    }

    public int getInitCost() {
        return initCost;
    }
}
